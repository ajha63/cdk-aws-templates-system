    def test_property_1_naming_idempotency(self, params):
        """
        Feature: cdk-aws-templates-system
        Property 1 (Extended): Naming Idempotency - For any resource configuration,
        calling generate_name multiple times with the same parameters SHALL produce
        the same name.
        
        Validates: Requirements 1.2, 1.3
        """
