        # Generate name twice
        name1 = naming_service.generate_name(
            resource_type=params['resource_type'],
            purpose=params['purpose'],
            environment=params['environment'],
            region=params['region'],
            service=params['service'],
            instance_number=params['instance_number']
        )
        
        name2 = naming_service.generate_name(
            resource_type=params['resource_type'],
            purpose=params['purpose'],
            environment=params['environment'],
            region=params['region'],
            service=params['service'],
            instance_number=params['instance_number']
        )
        
        # Should be identical
        assert name1 == name2, \
            f"generate_name is not idempotent: '{name1}' != '{name2}'"

